/**
 * Report Generator
 *
 * Converts the latest eval JSON report into a markdown document
 * suitable for the README and docs site.
 *
 * Usage: pnpm --filter @microsaas/evals evals:report
 */

import { readFileSync, writeFileSync } from "fs";
import { join } from "path";
import type { EvalReport } from "./harness";

function main() {
    const reportPath = join(__dirname, "..", "reports", "latest.json");
    let report: EvalReport;

    try {
        report = JSON.parse(readFileSync(reportPath, "utf8"));
    } catch {
        console.error("No report found. Run `pnpm evals` first.");
        process.exit(1);
    }

    const lines: string[] = [];

    lines.push("# Eval Report");
    lines.push("");
    lines.push(`> Last run: ${report.timestamp}`);
    lines.push(`> Provider: \`${report.provider}\` (deterministic mode)`);
    lines.push("");

    // Summary table
    lines.push("## Summary");
    lines.push("");
    lines.push("| Metric | Value |");
    lines.push("|--------|-------|");
    lines.push(`| Total cases | ${report.totalCases} |`);
    lines.push(`| Passed | ${report.passed} |`);
    lines.push(`| Failed | ${report.failed} |`);
    lines.push(`| **Pass rate** | **${(report.passRate * 100).toFixed(0)}%** |`);
    lines.push(`| Schema validity | ${(report.metrics.schema_validity_rate * 100).toFixed(0)}% |`);
    lines.push(`| Completion rate | ${(report.metrics.completion_rate * 100).toFixed(0)}% |`);
    lines.push(`| Error rate | ${(report.metrics.error_rate * 100).toFixed(0)}% |`);
    lines.push(`| Avg latency | ${report.avgLatencyMs}ms |`);
    lines.push(`| P50 latency | ${report.metrics.p50_latency_ms}ms |`);
    lines.push(`| P95 latency | ${report.metrics.p95_latency_ms}ms |`);
    lines.push(`| Mean output length | ${report.metrics.mean_output_length} chars |`);
    lines.push("");

    // Per-case table
    lines.push("## Per-Case Results");
    lines.push("");
    lines.push("| Case ID | Tool | Pass | Latency | Output Len | Failed Checks |");
    lines.push("|---------|------|------|---------|------------|---------------|");

    for (const r of report.results) {
        const failedChecks = Object.entries(r.scores)
            .filter(([, v]) => !v)
            .map(([k]) => k.replace(/_/g, " "));
        const icon = r.passed ? "✅" : "❌";
        lines.push(
            `| ${r.id} | ${r.tool} | ${icon} | ${r.latencyMs}ms | ${r.outputLength} | ${failedChecks.join(", ") || "—"} |`
        );
    }

    lines.push("");
    lines.push("## Scoring Rubric");
    lines.push("");
    lines.push("Each case is scored on 6 deterministic checks:");
    lines.push("");
    lines.push("1. **schema_valid** — Output is non-null and non-empty");
    lines.push("2. **output_nonempty** — Trimmed output has content");
    lines.push("3. **min_lines_met** — Output meets minimum line count");
    lines.push("4. **length_within_limit** — Output doesn't exceed max length");
    lines.push("5. **contains_input_reference** — Output references the user's input");
    lines.push("6. **no_error_thrown** — No exception during generation");
    lines.push("");
    lines.push("A case passes only if all 6 checks pass.");
    lines.push("");
    lines.push("---");
    lines.push("*Generated by `@microsaas/evals` — run `pnpm evals && pnpm evals:report` to reproduce*");

    const md = lines.join("\n");
    const outPath = join(__dirname, "..", "reports", "REPORT.md");
    writeFileSync(outPath, md);
    console.log(`📄 Markdown report written to packages/evals/reports/REPORT.md`);

    // Also copy to docs for the web app
    const docsPath = join(__dirname, "..", "..", "..", "docs", "EVAL_REPORT.md");
    writeFileSync(docsPath, md);
    console.log(`📄 Copied to docs/EVAL_REPORT.md`);
}

main();
